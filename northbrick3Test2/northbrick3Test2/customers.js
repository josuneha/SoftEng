function createCell(tr1, name) {

    var td0 = document.createElement("TD");		// primera columna
	
    texto = document.createTextNode(name);
	
    td0.appendChild(texto);		//td0.appendChild(in0);
	
	td0.setAttribute("value", name);
	
	tr1.appendChild(td0);
	
	var td1 = document.createElement("TD");		// segunda columna de la fila
	
	var in1 = document.createElement("INPUT");
	
	in1.setAttribute("name", name);
	
	in1.setAttribute("placeholder", name);
	
	td1.appendChild(in1);
	
	tr1.appendChild(td1);
}

